package Model;

public class Doctor {
	
	private int doc_Id;
	private String doc_Name;
	private String doc_Type;
	private String doc_Email;
	private String doc_RegId;
	private String doc_Phone;
	private String doc_Password;
	public int getDoc_Id() {
		return doc_Id;
	}
	public void setDoc_Id(int doc_Id) {
		this.doc_Id = doc_Id;
	}
	public String getDoc_Name() {
		return doc_Name;
	}
	public void setDoc_Name(String doc_Name) {
		this.doc_Name = doc_Name;
	}
	public String getDoc_Type() {
		return doc_Type;
	}
	public void setDoc_Type(String doc_Type) {
		this.doc_Type = doc_Type;
	}
	public String getDoc_Email() {
		return doc_Email;
	}
	public void setDoc_Email(String doc_Email) {
		this.doc_Email = doc_Email;
	}
	public String getDoc_RegId() {
		return doc_RegId;
	}
	public void setDoc_RegId(String doc_RegId) {
		this.doc_RegId = doc_RegId;
	}
	public String getDoc_Phone() {
		return doc_Phone;
	}
	public void setDoc_Phone(String doc_Phone) {
		this.doc_Phone = doc_Phone;
	}
	public String getDoc_Password() {
		return doc_Password;
	}
	public void setDoc_Password(String doc_Password) {
		this.doc_Password = doc_Password;
	}


}
